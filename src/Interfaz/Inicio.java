    boolean ConsultaAdmin(){
        
        Connection connection = con.iniciarConexion();
        
        String Password = txtPassword.getText().trim();
        String NombreUsuario = txtUsuario.getText().toLowerCase().trim();
        
        boolean encontro=false;
        String codigo="";
        
        String sql = "SELECT CODIGO_USUARIO FROM LOGIN WHERE PASSWORD = '"+Password+"' AND USUARIO = '"+NombreUsuario+"'";
        
         
         try {
            Statement st = connection.createStatement();
            ResultSet rs = st.executeQuery(sql);
                    
            while (rs.next()){
                codigo = rs.getString("CODIGO_USUARIO");
                encontro=true;
            }
                        
        } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, ex);
        }
        
        return encontro;
    }

