    void LlenarTabla(String sql, String[] datos){
        
        Connection connection = con.iniciarConexion();
        String valores[]=new String[5];
        
        DefaultTableModel modelo = new DefaultTableModel(null, datos);
        
        try {
            Statement st2 = connection.createStatement();
            ResultSet rs2 = st2.executeQuery(sql);
                    
            while (rs2.next()){
                 
                if(contador==0){
                 valores[0] = rs2.getString("NOMBRE");
                 valores[1] = rs2.getString("APELLIDO");
                 valores[2] = rs2.getString("ALTURA");
                 valores[3] = rs2.getString("PESO");  
                }else if(contador==1){
                    valores[0] = rs2.getString("NOMBRE");
                    valores[1] = rs2.getString("APELLIDO");
                    valores[2] = rs2.getString("PESO");
                }else if(contador==2){
                    valores[0] = rs2.getString("NOMBRE");
                    valores[1] = rs2.getString("APELLIDO");
                    valores[2] = rs2.getString("ALTURA");
                }
                 
                 modelo.addRow(valores);
            }
                        
        } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, ex);
        }
           
            TablaResultados.setModel(modelo);
            int[] anchos = {100, 80, 80,100};

            //hacemos un bucle FOR desde cero hasta la cantidad de columnas de nuestra tabla

            for(int i = 0; i < TablaResultados.getColumnCount(); i++) {
                TablaResultados.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
            }
    }

