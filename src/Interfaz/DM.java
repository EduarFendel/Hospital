    private void rbAlergiaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rbAlergiaMouseClicked
        // TODO add your handling code here:
        cmbBuscar.removeAllItems();
        btnBuscar.setVisible(true);
        cmbBuscar.setVisible(true);
        txtBuscar.setVisible(false);
        Connection connection = con.iniciarConexion();
        String Alergia = "";
        String sql = "SELECT NOMBRE_ALERGIA FROM ALERGIA";
        
            
           try {
            Statement st2 = connection.createStatement();
            ResultSet rs2 = st2.executeQuery(sql);
                    
            while (rs2.next()){
                 Alergia = rs2.getString("NOMBRE_ALERGIA");
                 cmbBuscar.addItem(Alergia);
                 Alergia = "";
                 
            }
                        
        } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_rbAlergiaMouseClicked

